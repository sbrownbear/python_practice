# принимает купюры, он принимает только чеканные монеты. В мире ведьмака существуют монеты с номиналами 1, 5, 10, 25. Напишите программу, 
# которая определяет какое минимальное количество чеканных монет нужно заплатить ведьмаку.
# Вариант решения №1

